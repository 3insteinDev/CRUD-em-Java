package info.fut.model;

public class timedefutebol {
	
	private int id;
	private String nome;
	private String estado;
	private int titulos;
	private int rebaixamentos;
	private int torcedores;
	private String mascote;
	
	// -------------Getters e Setters ------------
	
	//--------------ID------------------
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	//--------------NOME------------------
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public int getTitulos() {
		return titulos;
	}
	public void setTitulos(int titulos) {
		this.titulos = titulos;
	}
	public int getRebaixamentos() {
		return rebaixamentos;
	}
	public void setRebaixamentos(int rebaixamentos) {
		this.rebaixamentos = rebaixamentos;
	}
	public int getTorcedores() {
		return torcedores;
	}
	public void setTorcedores(int torcedores) {
		this.torcedores = torcedores;
	}
	public String getMascote() {
		return mascote;
	}
	public void setMascote(String mascote) {
		this.mascote = mascote;
	}
	
	
	
}

